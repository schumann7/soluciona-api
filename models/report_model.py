class Report:
    def __init__(self, id, name, latitude, longitude, description, place, address, status, registered_by, registered_date):
        self.id = id
        self.name = name
        self.latitude = latitude
        self.longitude = longitude
        self.description = description
        self.place = place
        self.address = address
        self.status = status
        self.registered_by = registered_by
        self.registered_date = registered_date